"""
Compare diagonal Fisher DL vs full-determinant DL for top ESR and literature
functions across all datasets (LF Sérsic, LF cmodel, SMF Sérsic, SMF cmodel, HMF).

Supports the DL-convention discussion in Sec 4.5: the ESR code length formula
uses the diagonal Fisher approximation, and this script quantifies how much
the result changes if one uses the full determinant instead.

Diagonal:  codelen = -k/2*ln(3) + sum_i [0.5*ln(H_ii) + ln|theta_i|]
Full det:  codelen = -k/2*ln(3) + 0.5*ln(det(H)) + sum_i ln|theta_i|
Delta:     delta   = 0.5*[ln(det(H)) - sum_i ln(H_ii)]

If parameters are correlated, det(H) < prod(H_ii), so delta < 0.

Inputs:
  - {dataset}_final_functions.txt  (LF/SMF/HMF final function tables)
  - data/LF_Ser_L.txt, data/SMF_Ser_M.txt, data/SMF_cmodel_M.txt (LF/SMF data)
  - data/LF_cmodel_L.dat (generated by create_LF_cmodel_L_data.py)
  - data/hmf_files/hmf_50.dat (reference HMF sim)
  - hmf_combined_DL.txt, literature_fits_all_sims.txt
  - hmf_data/hmf_<sim>_data/final_all.txt

Outputs:
  - fisher_det_results.txt  (full per-function comparison and ranking tables)

Dependencies:
  numpy, numdifftools
"""

import numpy as np
import numdifftools as nd
import itertools
import warnings
import re
import os

warnings.filterwarnings("ignore")

# ── Data loading ───────────────────────────────────────────────────────────

def load_lf_smf_data(path):
    """Load 4-column LF/SMF data: x_raw, log10(phi), err, Veff.
    Returns x (in units of 1e9 L_sun or M_sun), counts = 10^y * Veff, Veff.

    Note: some files (LF_Ser_L.txt, SMF_*.txt) already have x in 1e9 units,
    while LF_cmodel_L.dat has x in raw units (needs *1e-9).
    """
    data = np.loadtxt(path)
    x_raw = data[:, 0]
    y = data[:, 1]
    Veff = data[:, 3]
    counts = 10**(y) * Veff

    # If x values are very large (>1e6), they're in raw units and need scaling
    if x_raw.max() > 1e6:
        x = x_raw * 1e-9
    else:
        x = x_raw

    return x, counts, Veff


def load_hmf_data(path):
    """Load HMF data: sigma, counts, err, norm, log10(M).
    Returns sigma, counts, norm."""
    data = np.loadtxt(path)
    return data[:, 0], data[:, 1], data[:, 3]


# ── Function evaluator ────────────────────────────────────────────────────

def make_evaluator(template_str):
    """Create numpy-evaluable function from ESR template string.
    ESR convention: pow(a,b) = |a|^b, log(a) = ln|a|."""
    # Find parameter names
    param_names = sorted(set(re.findall(r'a\d+', template_str)))

    def evaluator(params, x):
        ns = {
            'x': x,
            'pow': lambda a, b: np.float_power(np.abs(a), b),
            'Abs': np.abs, 'abs': np.abs,
            'exp': np.exp,
            'log': lambda a: np.log(np.abs(a)),
            'sqrt': lambda a: np.sqrt(np.abs(a)),
        }
        for name, val in zip(param_names, params):
            ns[name] = val
        with np.errstate(all='ignore'):
            result = eval(template_str, {"__builtins__": {}}, ns)
        return np.asarray(result, dtype=float)
    return evaluator, param_names


def extract_params_from_fitted(fitted_str, template_str):
    """Extract numeric parameter values from a fitted function string
    by comparing with the template.

    Strategy: Build a regex from the template by replacing each parameter
    occurrence (a0, a1, ...) with a float-capturing group. Handle parameters
    that appear multiple times by using backreferences.
    """
    param_names = sorted(set(re.findall(r'a\d+', template_str)))
    if not param_names:
        return np.array([])

    # Build regex: escape the template, then replace parameter occurrences
    # For repeated parameters, first occurrence gets a capture group,
    # subsequent occurrences match the same value.
    float_re = r'([+-]?(?:\d+\.?\d*|\.\d+)(?:[eE][+-]?\d+)?)'

    # Work character by character through template to build regex
    regex_pat = re.escape(template_str)
    group_map = {}  # param_name -> group_number
    group_num = 0

    for pname in sorted(param_names, key=lambda p: int(p[1:]), reverse=True):
        # Replace longest param names first to avoid a1 matching part of a10
        escaped_pname = re.escape(pname)
        # Count occurrences
        occurrences = len(re.findall(escaped_pname, regex_pat))
        if occurrences == 0:
            continue

        if pname not in group_map:
            group_num += 1
            group_map[pname] = group_num
            # Replace first occurrence with capture group, rest with same float pattern
            # (we can't use backreferences because the same param may have different formatting)
            regex_pat = regex_pat.replace(escaped_pname, float_re)

    m = re.match(regex_pat, fitted_str)
    if m:
        # Extract one value per unique parameter
        # Since we replaced all occurrences, take every Nth group for N-occurrence params
        all_groups = list(m.groups())
        # Map back: for each param_name in order, find its first captured value
        result = []
        idx = 0
        occurrences_per_param = {}
        for pname in sorted(param_names, key=lambda p: int(p[1:]), reverse=True):
            escaped_pname = re.escape(pname)
            occurrences_per_param[pname] = len(re.findall(re.escape(pname), re.escape(template_str)))

        # Re-derive by scanning the template for parameter positions
        param_positions = []  # (position_in_template, param_name)
        for pname in param_names:
            for m2 in re.finditer(r'\b' + re.escape(pname) + r'\b', template_str):
                param_positions.append((m2.start(), pname))
        param_positions.sort()

        # The regex groups correspond to these positions in order
        seen_params = {}
        values = {}
        for i, (pos, pname) in enumerate(param_positions):
            if i < len(all_groups):
                if pname not in values:
                    values[pname] = float(all_groups[i])

        if len(values) == len(param_names):
            return np.array([values[p] for p in param_names])

    # Fallback: scan for all floats, identify which are parameters
    # by removing template-intrinsic numbers
    def extract_floats(s):
        return [float(x) for x in re.findall(
            r'[+-]?(?:\d+\.?\d*|\.\d+)(?:[eE][+-]?\d+)?', s)]

    fitted_floats = extract_floats(fitted_str)
    template_floats = extract_floats(template_str)

    # Remove template floats from fitted floats
    remaining = list(fitted_floats)
    for tf in template_floats:
        for i, rf in enumerate(remaining):
            if abs(rf - tf) < 1e-10 * max(1, abs(tf)):
                remaining.pop(i)
                break

    # For templates with repeated params (like Schechter), we expect
    # fewer unique numbers than float occurrences
    unique_remaining = []
    seen = set()
    for r in remaining:
        key = f"{r:.10e}"
        if key not in seen:
            seen.add(key)
            unique_remaining.append(r)

    if len(unique_remaining) >= len(param_names):
        return np.array(unique_remaining[:len(param_names)])

    if len(remaining) >= len(param_names):
        return np.array(remaining[:len(param_names)])

    # Last resort: just take first N floats from fitted string
    if len(fitted_floats) >= len(param_names):
        return np.array(fitted_floats[:len(param_names)])

    return None


# ── Poisson NLL ────────────────────────────────────────────────────────────

def poisson_nll_lf_smf(params, x, counts, Veff, func):
    """Poisson NLL for LF/SMF: lambda = f(x) * Veff."""
    with np.errstate(all='ignore'):
        f_vals = func(params, x)
    lam = f_vals * Veff
    if np.any(lam <= 0) or np.any(~np.isfinite(lam)):
        return np.inf
    nll = np.sum(lam - counts * np.log(lam))
    return nll if np.isfinite(nll) else np.inf


def poisson_nll_hmf(params, sigma, counts, norm, func):
    """Poisson NLL for HMF: lambda = f(sigma) * norm."""
    with np.errstate(all='ignore'):
        f_vals = func(params, sigma)
    lam = f_vals * norm
    if np.any(lam <= 0) or np.any(~np.isfinite(lam)):
        return np.inf
    nll = np.sum(lam - counts * np.log(lam))
    return nll if np.isfinite(nll) else np.inf


# ── Hessian computation ───────────────────────────────────────────────────

def compute_hessian(nll_func, params):
    """Compute the full Hessian matrix using numdifftools with robust step sizes."""
    k = len(params)
    if k == 0:
        return None

    d_list = [1e-5, 10**(-5.5), 10**(-4.5), 1e-6, 1e-4,
              10**(-6.5), 10**(-3.5), 1e-7, 1e-3, 10**(-7.5),
              10**(-2.5), 1e-8, 1e-2]
    method_list = ["central", "forward", "backward"]

    best_H = None
    best_cond = np.inf

    # Try default first
    try:
        H = nd.Hessian(nll_func)(params)
        diag = np.array([H[i, i] for i in range(k)])
        if np.all(diag > 0) and np.all(np.isfinite(H)):
            det_val = np.linalg.det(H)
            if det_val > 0:
                cond = np.linalg.cond(H)
                if cond < best_cond:
                    best_H = H.copy()
                    best_cond = cond
    except Exception:
        pass

    # Try relative step sizes
    for d2, meth in itertools.product(d_list, method_list):
        try:
            step = np.abs(d2 * params) + 1e-15
            H = nd.Hessian(nll_func, step=step, method=meth)(params)
            diag = np.array([H[i, i] for i in range(k)])
            if np.all(diag > 0) and np.all(np.isfinite(H)):
                det_val = np.linalg.det(H)
                if det_val > 0:
                    cond = np.linalg.cond(H)
                    if cond < best_cond:
                        best_H = H.copy()
                        best_cond = cond
                    if best_cond < 1e10:
                        break  # Good enough
        except Exception:
            continue

    return best_H


# ── DL computation ─────────────────────────────────────────────────────────

def compute_codelen_comparison(H, params):
    """Compute diagonal and full-determinant code lengths.
    Returns (codelen_diag, codelen_det, delta) or (None, None, None) on failure."""
    k = len(params)
    if k == 0 or H is None:
        return 0.0, 0.0, 0.0

    diag = np.array([H[i, i] for i in range(k)])

    # Check diagonal validity
    if np.any(diag <= 0) or np.any(~np.isfinite(diag)):
        return None, None, None

    # Check Nsteps
    Delta = np.sqrt(12.0 / diag)
    Nsteps = np.abs(params) / Delta

    # For params with Nsteps < 1, mimic ESR's approach but keep for comparison
    mask = Nsteps >= 1
    k_eff = int(np.sum(mask))
    if k_eff == 0:
        return 0.0, 0.0, 0.0

    theta_active = params[mask]
    diag_active = diag[mask]
    H_active = H[np.ix_(mask, mask)]

    # Diagonal codelen
    codelen_diag = (-k_eff / 2.0 * np.log(3.0)
                    + np.sum(0.5 * np.log(diag_active) + np.log(np.abs(theta_active))))

    # Full determinant codelen
    det_H = np.linalg.det(H_active)
    if det_H <= 0 or not np.isfinite(det_H):
        # Try log-det via slogdet
        sign, logdet = np.linalg.slogdet(H_active)
        if sign <= 0:
            return codelen_diag, None, None
        codelen_det = (-k_eff / 2.0 * np.log(3.0)
                       + 0.5 * logdet + np.sum(np.log(np.abs(theta_active))))
    else:
        codelen_det = (-k_eff / 2.0 * np.log(3.0)
                       + 0.5 * np.log(det_H) + np.sum(np.log(np.abs(theta_active))))

    delta = codelen_det - codelen_diag

    return codelen_diag, codelen_det, delta


# ── Parse final_functions.txt ──────────────────────────────────────────────

def parse_final_functions(filepath):
    """Parse a *_final_functions.txt file.
    Returns list of dicts with keys: source, complexity, DL, NLL, fitted_str, template_str."""
    entries = []
    with open(filepath, 'r') as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith('#'):
                continue
            parts = line.split(';')
            if len(parts) < 6:
                continue
            entries.append({
                'source': parts[0],
                'complexity': int(parts[1]),
                'DL': float(parts[2]),
                'NLL': float(parts[3]),
                'fitted_str': parts[4],
                'template_str': parts[5],
            })
    return entries


def parse_hmf_combined(filepath):
    """Parse hmf_combined_DL.txt.
    Returns list of dicts with keys: rank, template_str, DL_combined, etc."""
    entries = []
    with open(filepath, 'r') as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith('#'):
                continue
            parts = line.split(';')
            if len(parts) < 8:
                continue
            entries.append({
                'rank': int(parts[0]),
                'template_str': parts[1],
                'DL_combined': float(parts[2]),
                'delta_DL': float(parts[3]),
                'sum_NLL': float(parts[4]),
                'sum_DL': float(parts[5]),
                'aifeynman': float(parts[6]),
                'n_sims': int(parts[7]),
            })
    return entries


# ── HMF literature functions ──────────────────────────────────────────────

def press_schechter(params, x):
    delta_c = 1.686
    return np.sqrt(2.0 / np.pi) * (delta_c / x) * np.exp(-0.5 * (delta_c / x)**2)

def warren_func(params, x):
    a0, a1, a2, a3 = params
    return a0 * (np.float_power(np.abs(x), a2) + a1) * np.exp(-a3 * np.float_power(np.abs(x), -2.0))

def tinker_func(params, x):
    a0, a1, a2, a3 = params
    return a0 * (np.float_power(np.abs(x / a2), -a1) + 1.0) * np.exp(-a3 * np.float_power(np.abs(x), -2.0))


# ── HMF per-sim ESR results ───────────────────────────────────────────────

def load_hmf_sim_results(sim):
    """Load final_all.txt for a given HMF sim.
    Format: rank;template;DL;NLL;[params]"""
    filepath = f'hmf_data/hmf_{sim}_data/final_all.txt'
    entries = []
    with open(filepath, 'r') as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            parts = line.split(';')
            if len(parts) < 4:
                continue
            rank = int(parts[0])
            template = parts[1]
            DL = float(parts[2])
            NLL = float(parts[3])
            # Parse params from e.g. "[0.65734271 1.70541354 1.63275111 2.78494662]"
            param_str = parts[4].strip('[] ')
            params = np.array([float(x) for x in param_str.split()])
            entries.append({
                'rank': rank,
                'template_str': template,
                'DL': DL,
                'NLL': NLL,
                'params': params,
            })
    return entries


# ── Main analysis ──────────────────────────────────────────────────────────

def analyze_lf_smf(dataset_name, functions_file, data_file, outlines):
    """Analyze one LF/SMF dataset."""
    outlines.append(f"\n{'='*80}")
    outlines.append(f"  {dataset_name}")
    outlines.append(f"{'='*80}\n")

    x, counts, Veff = load_lf_smf_data(data_file)
    entries = parse_final_functions(functions_file)

    # Deduplicate: keep unique (source, template_str) pairs
    seen = set()
    unique_entries = []
    for e in entries:
        key = (e['source'], e['template_str'])
        if key not in seen:
            seen.add(key)
            unique_entries.append(e)
    entries = unique_entries

    results = []
    for e in entries:
        source = e['source']
        template = e['template_str']
        fitted = e['fitted_str']
        DL_orig = e['DL']
        NLL_orig = e['NLL']

        func, param_names = make_evaluator(template)
        k = len(param_names)

        if k == 0:
            results.append((source, e['complexity'], k, DL_orig, 0.0, 0.0, 0.0, DL_orig, "no params"))
            continue

        # Extract parameters
        params = extract_params_from_fitted(fitted, template)
        if params is None or len(params) != k:
            results.append((source, e['complexity'], k, DL_orig, None, None, None, None, "param extraction failed"))
            continue

        # Build NLL function
        def nll_func(p, _func=func, _x=x, _counts=counts, _Veff=Veff):
            return poisson_nll_lf_smf(p, _x, _counts, _Veff, _func)

        # Verify NLL
        nll_check = nll_func(params)
        if not np.isfinite(nll_check):
            results.append((source, e['complexity'], k, DL_orig, None, None, None, None, "NLL not finite"))
            continue

        # Compute Hessian
        H = compute_hessian(nll_func, params)
        if H is None:
            results.append((source, e['complexity'], k, DL_orig, None, None, None, None, "Hessian failed"))
            continue

        codelen_diag, codelen_det, delta = compute_codelen_comparison(H, params)

        if codelen_diag is None or codelen_det is None:
            results.append((source, e['complexity'], k, DL_orig, codelen_diag, codelen_det, None, None, "det failed"))
            continue

        DL_new = DL_orig + delta
        results.append((source, e['complexity'], k, DL_orig, codelen_diag, codelen_det, delta, DL_new, "OK"))

    # Print table
    header = f"{'Source':<8} {'Comp':>4} {'k':>2} {'DL_orig':>16} {'CL_diag':>12} {'CL_det':>12} {'delta':>10} {'DL_new':>16} {'Status'}"
    outlines.append(header)
    outlines.append("-" * len(header))

    for r in results:
        source, comp, k, DL_orig, cl_diag, cl_det, delta, DL_new, status = r
        DL_orig_s = f"{DL_orig:.2f}" if DL_orig is not None else "N/A"
        cl_diag_s = f"{cl_diag:.4f}" if cl_diag is not None else "N/A"
        cl_det_s = f"{cl_det:.4f}" if cl_det is not None else "N/A"
        delta_s = f"{delta:.4f}" if delta is not None else "N/A"
        DL_new_s = f"{DL_new:.2f}" if DL_new is not None else "N/A"
        outlines.append(f"{source:<8} {comp:>4} {k:>2} {DL_orig_s:>16} {cl_diag_s:>12} {cl_det_s:>12} {delta_s:>10} {DL_new_s:>16} {status}")

    # Show ranking comparison
    outlines.append("")
    ok_results = [(r[0], r[1], r[3], r[7], r[6]) for r in results if r[8] == "OK" and r[7] is not None]
    if ok_results:
        # Sort by original DL (more negative = better)
        ok_orig = sorted(ok_results, key=lambda x: x[2])
        ok_new = sorted(ok_results, key=lambda x: x[3])

        outlines.append("Ranking comparison (lower DL = better):")
        outlines.append(f"  {'Rank':>4} {'Orig: Source':<10} {'DL_orig':>16}  |  {'New: Source':<10} {'DL_new':>16} {'delta':>10}")
        outlines.append(f"  {'-'*4} {'-'*10} {'-'*16}  |  {'-'*10} {'-'*16} {'-'*10}")
        for i in range(len(ok_orig)):
            o = ok_orig[i]
            n = ok_new[i]
            outlines.append(f"  {i+1:>4} {o[0]+' C'+str(o[1]):<10} {o[2]:>16.2f}  |  {n[0]+' C'+str(n[1]):<10} {n[3]:>16.2f} {n[4]:>10.4f}")

    return results


def analyze_hmf(outlines):
    """Analyze HMF using sim 50 as representative."""
    outlines.append(f"\n{'='*80}")
    outlines.append(f"  HMF (sim 50 as representative)")
    outlines.append(f"{'='*80}\n")

    sigma, counts, norm = load_hmf_data('data/hmf_files/hmf_50.dat')

    # Load per-sim ESR results for sim 50
    sim_results = load_hmf_sim_results(50)

    # Load combined DL rankings to identify top functions
    combined = parse_hmf_combined('hmf_combined_DL.txt')
    top_templates = [c['template_str'] for c in combined[:10]]

    # Load literature fit parameters for sim 50
    lit_params = {}
    with open('literature_fits_all_sims.txt', 'r') as f:
        for line in f:
            if line.startswith('#'):
                continue
            parts = line.strip().split(';')
            if len(parts) >= 6 and parts[1] == '50':
                name = parts[0]
                if parts[5] != 'none':
                    lit_params[name] = np.array([float(x) for x in parts[5].split()])
                else:
                    lit_params[name] = None

    results = []

    # Process top ESR functions
    for template in top_templates:
        # Find this template in sim 50 results
        match = None
        for sr in sim_results:
            if sr['template_str'] == template:
                match = sr
                break

        if match is None:
            results.append(("ESR", template[:40], 0, None, None, None, None, None, "not found in sim 50"))
            continue

        params = match['params']
        DL_orig = match['DL']
        func, param_names = make_evaluator(template)
        k = len(params)

        def nll_func(p, _func=func, _sigma=sigma, _counts=counts, _norm=norm):
            return poisson_nll_hmf(p, _sigma, _counts, _norm, _func)

        nll_check = nll_func(params)
        if not np.isfinite(nll_check):
            results.append(("ESR", template[:40], k, DL_orig, None, None, None, None, "NLL not finite"))
            continue

        H = compute_hessian(nll_func, params)
        if H is None:
            results.append(("ESR", template[:40], k, DL_orig, None, None, None, None, "Hessian failed"))
            continue

        codelen_diag, codelen_det, delta = compute_codelen_comparison(H, params)

        if codelen_diag is None or codelen_det is None:
            results.append(("ESR", template[:40], k, DL_orig, codelen_diag, codelen_det, None, None, "det failed"))
            continue

        DL_new = DL_orig + delta
        results.append(("ESR", template[:40], k, DL_orig, codelen_diag, codelen_det, delta, DL_new, "OK"))

    # Process literature functions
    # Press-Schechter (no free params)
    results.append(("P.Sch.", "sqrt(2/pi)*dc/x*exp(-dc^2/2x^2)", 0, None, 0.0, 0.0, 0.0, None, "no params"))

    # Warren
    if 'War.' in lit_params and lit_params['War.'] is not None:
        params = lit_params['War.']
        def nll_func_w(p, _sigma=sigma, _counts=counts, _norm=norm):
            return poisson_nll_hmf(p, _sigma, _counts, _norm, warren_func)

        nll_check = nll_func_w(params)
        H = compute_hessian(nll_func_w, params) if np.isfinite(nll_check) else None
        if H is not None:
            codelen_diag, codelen_det, delta = compute_codelen_comparison(H, params)
            if codelen_diag is not None and codelen_det is not None:
                results.append(("War.", "a0*(x^a2+a1)*exp(-a3*x^-2)", 4, None, codelen_diag, codelen_det, delta, None, "OK"))
            else:
                results.append(("War.", "a0*(x^a2+a1)*exp(-a3*x^-2)", 4, None, codelen_diag, codelen_det, None, None, "det failed"))
        else:
            results.append(("War.", "a0*(x^a2+a1)*exp(-a3*x^-2)", 4, None, None, None, None, None, "Hessian failed"))

    # Tinker
    if 'Tin.' in lit_params and lit_params['Tin.'] is not None:
        params = lit_params['Tin.']
        def nll_func_t(p, _sigma=sigma, _counts=counts, _norm=norm):
            return poisson_nll_hmf(p, _sigma, _counts, _norm, tinker_func)

        nll_check = nll_func_t(params)
        H = compute_hessian(nll_func_t, params) if np.isfinite(nll_check) else None
        if H is not None:
            codelen_diag, codelen_det, delta = compute_codelen_comparison(H, params)
            if codelen_diag is not None and codelen_det is not None:
                results.append(("Tin.", "a0*((x/a2)^-a1+1)*exp(-a3*x^-2)", 4, None, codelen_diag, codelen_det, delta, None, "OK"))
            else:
                results.append(("Tin.", "a0*((x/a2)^-a1+1)*exp(-a3*x^-2)", 4, None, codelen_diag, codelen_det, None, None, "det failed"))
        else:
            results.append(("Tin.", "a0*((x/a2)^-a1+1)*exp(-a3*x^-2)", 4, None, None, None, None, None, "Hessian failed"))

    # Print table
    header = f"{'Source':<8} {'Function':<42} {'k':>2} {'CL_diag':>12} {'CL_det':>12} {'delta':>10} {'Status'}"
    outlines.append(header)
    outlines.append("-" * len(header))

    for r in results:
        source, func_str, k, DL_orig, cl_diag, cl_det, delta, DL_new, status = r
        cl_diag_s = f"{cl_diag:.4f}" if cl_diag is not None else "N/A"
        cl_det_s = f"{cl_det:.4f}" if cl_det is not None else "N/A"
        delta_s = f"{delta:.4f}" if delta is not None else "N/A"
        outlines.append(f"{source:<8} {func_str:<42} {k:>2} {cl_diag_s:>12} {cl_det_s:>12} {delta_s:>10} {status}")

    # Summary for HMF
    outlines.append("")
    outlines.append("Note: For the combined DL across 100 sims, multiply delta by ~100")
    outlines.append("(assuming similar parameter correlations across sims).")
    outlines.append("")

    ok_esr = [r for r in results if r[0] == "ESR" and r[8] == "OK"]
    if ok_esr:
        deltas = [r[6] for r in ok_esr]
        outlines.append(f"ESR delta range: [{min(deltas):.4f}, {max(deltas):.4f}]")
        outlines.append(f"Estimated combined delta range: [{100*min(deltas):.1f}, {100*max(deltas):.1f}]")

    return results


